"""App Factory — reusable scaffold for SHIMS vertical apps.

This module gives SHIMS Omni (and its Coder / Swarm agents) deterministic
helpers to create, mount, and extend self-contained FastAPI apps under
``apps/<name>/``.  It is intentionally plain: no LLM calls, no risky
auto-wiring of live source files beyond the allowed app directory.

Typical usage from a tool or agent:

    from shared.app_factory import ensure_app_package, create_app_template_files
    ensure_app_package("todo_demo")
    create_app_template_files("todo_demo", prefix="/todo", title="Todo Demo")

Then the agent fills domain-specific services, routes, templates, and tests.
"""
