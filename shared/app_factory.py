STORAGE_DIR = Path(settings.database_path).parent
TEMPLATES_DIR = ROOT_DIR / "frontend"


def derive_paths(app_name: str) -> dict[str, Path]:
    """Return the canonical paths for a new vertical app."""
    app_dir = APPS_DIR / app_name
    return {
        "app_dir": app_dir,
        "services_dir": app_dir / "services",
        "templates_dir": app_dir / "templates",
        "static_dir": app_dir / "static",
        "css_dir": app_dir / "static" / "css",
        "js_dir": app_dir / "static" / "js",
        "routers_dir": app_dir / "routers",
        "tests_dir": ROOT_DIR / "tests",
        "db_path": STORAGE_DIR / f"{app_name}.sqlite3",
    }


def ensure_app_package(app_name: str) -> dict[str, Path]:
    """Create the app folder skeleton (idempotent)."""
    paths = derive_paths(app_name)
    for d in ["services_dir", "templates_dir", "css_dir", "js_dir", "routers_dir"]:
        paths[d].mkdir(parents=True, exist_ok=True)
    # Make the package importable.
    for init in (paths["app_dir"] / "__init__.py", paths["routers_dir"] / "__init__.py", paths["services_dir"] / "__init__.py"):
        if not init.exists():
            init.write_text("# auto-generated by shared/app_factory.py\n", encoding="utf-8")
    return paths


def mount_app_static(app: FastAPI, app_name: str, prefix: str | None = None) -> None:
    """Mount an app's static directory at ``/<app_name>-static``."""
    paths = derive_paths(app_name)
    static_dir = paths["static_dir"]
    static_dir.mkdir(parents=True, exist_ok=True)
    mount_path = f"/{(prefix or app_name).strip('/')}-static"
    # Unmount first to allow idempotent re-runs in tests / hot reload.
    existing = [r for r in app.routes if getattr(r, "name", None) == f"{app_name}-static"]
    for route in existing:
        app.routes.remove(route)
    app.mount(mount_path, StaticFiles(directory=str(static_dir)), name=f"{app_name}-static")


def register_app_router(app: FastAPI, router: APIRouter) -> None:
    """Include a router if its prefix is not already registered."""
    prefixes = {getattr(r, "path", "") for r in app.routes}
    if router.prefix and router.prefix in prefixes:
        return
    app.include_router(router)


def _write_if_missing(path: Path, content: str) -> bool:
    """Write ``content`` only if the file does not already exist."""
    if path.exists():
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")
    return True


