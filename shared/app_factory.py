def remove_app_package(app_name: str) -> dict[str, Any]:
    """Delete an app folder and its static mount (destructive; for sandbox/tests)."""
    paths = derive_paths(app_name)
    if paths["app_dir"].exists():
        shutil.rmtree(paths["app_dir"])
    return {"ok": True, "removed": str(paths["app_dir"])}
